    private void btLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btLoginActionPerformed

        String usuario = txtLogin.getText().trim();
        String senha = txtSenha.getText();

        if (usuario.equalsIgnoreCase("usuario") && senha.equals("123")) {
            Listagem listagem = new Listagem();
            listagem.Permissao(3); // Usuário
            listagem.setVisible(true);
            dispose(); // Fecha a janela de login
        } else if (usuario.equalsIgnoreCase("Administrador") && senha.equals("adm")) {
            Listagem listagem = new Listagem();
            listagem.Permissao(1); // Administrador
            listagem.setVisible(true);
            dispose(); // Fecha a janela de login
        } else if (usuario.equalsIgnoreCase("Operador") && senha.equals("opr")) {
            Listagem listagem = new Listagem();
            listagem.Permissao(2); // Operador
            listagem.setVisible(true);
            dispose(); // Fecha a janela de login
        } else {
            JOptionPane.showMessageDialog(null, "Acesso negado, login ou senha inválida");
        }


    }//GEN-LAST:event_btLoginActionPerformed

