    public void permissao(int idPermissao) {
        Cadastro telaCadastro = null;
        Listagem telaListagem = null;
        switch (idPermissao) {
            case 1: // Administrador
                JOptionPane.showMessageDialog(null, "Olá, sua permissão é de Administrador. Seja bem-vindo!");
                telaCadastro = new Cadastro();
                telaCadastro.setVisible(true);
                break;
            case 2: // Operador
                JOptionPane.showMessageDialog(null, "Olá, sua permissão é de Operador. Seja bem-vindo!");
                telaCadastro = new Cadastro();
                telaCadastro.setVisible(true);
                break;
            case 3: // Usuário
                JOptionPane.showMessageDialog(null, "Olá, sua permissão é de Usuário. Seja bem-vindo!");
                telaListagem = new Listagem();
                telaListagem.setVisible(true);
                break;
            default:
                JOptionPane.showMessageDialog(null, "Acesso negado, permissão inválida");
                break;
        }
    }

